  </p>
  <h4>Joe</h4>
  <p>
    With Col in your party, go to Amen Corner and find Joe in a dead end on the northwestern road, next to Deke&#39;s mansion. Speak to Joe, and agree to help him. Clear the dungeon to complete the quest.
  </p>
  <h4>Mike</h4>
  <p>
    Find Mike at the pub in southwestern Mayfall City. Hire him for 400 Arn.
  </p>
  <h4>Outsider</h4>
  <p>
    Find Outsider at the pub in southwestern Mayfall City. Hire him for 1,500 Arn.
  </p>
  <h4>Francesca</h4>
  <p>
    With a dark-elemental character in your party, speak to Francesca inside her house in northern Matrech State Capital.
  </p>
  <p>
    Wilona
    &nbsp;-
    Castle Function: Statistics Record
  </p>
  <p>
    Find Wilona inside her house in southeast Mayfall City. She will rate your active party&#39;s total STR or MAG value (entourage doesn&rsquo;t count). Build a total of 500 STR or 400 MAG to receive an A-rank from her, and she will join you.
  </p>
  <p>
    Note: Available as early as Chapter 3, but not likely to recruit until Chapter 6, due to the high stat requirement.
  </p>

  <h4>Midian (Method 1)</h4>
  <p>
    After recruiting Sally, have her take you to Midian&#39;s Island. Inside Midian&#39;s house, push the box located in the center of the room to the right side of the bookshelf with the chest on it, then proceed to try to reach the chest.
  </p>
  <p>
    Note: This method will not work after the start of Chapter 6.
  </p>

